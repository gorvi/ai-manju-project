#!/bin/bash
# ============================================================
# 《我老公是条龙》第1集 新制作脚本 v2
# 生成模式：
#   - text2video    → 纯环境/无角色镜头
#   - image2video   → 含角色镜头（scene底图 + 角色参考图双垫）
#   - multiframe2video → 2个场景间连贯过渡（同场景不同角度）
#
# 铁律：所有出现角色的镜头必须垫角色参考图
# ============================================================

OUT="/Users/ghw/Documents/xiaoshuo/ai-manju-project/05_出图/镜头素材/ep01"
mkdir -p "$OUT"

# ---------- 角色参考图 ----------
SNAKE_REF="/Users/ghw/Documents/xiaoshuo/ai-manju-project/05_出图/角色/敖玄_白蛇/aoxuan_snake_ref.png"
NVZHU_REF="/Users/ghw/Documents/xiaoshuo/ai-manju-project/05_出图/角色/沈映晚/shenyingwan_fenhong_ref.png"

# ---------- 场景底图 ----------
POMIAO="/Users/ghw/Documents/xiaoshuo/ai-manju-project/05_出图/场景底图_16x9_v50/破庙/scene_pomiao_16x9_v50.png"
POMIAO_CORNER="/Users/ghw/Documents/xiaoshuo/ai-manju-project/05_出图/场景底图_16x9_v50/破庙内角落/scene_pomiao_corner_16x9_v50.png"

# ============================================================
# 镜头1（5s）：雷云天空
# 无角色，text2video 纯文生
# ============================================================
echo ">>> [镜头1] 雷云天空 — text2video 5s"
dreamina text2video \
  --prompt "影视真人风格，古风玄幻，电影感光影。乌云压顶的天空，雷电撕裂天幕，黑云翻滚，灾厄感强烈，古风玄幻氛围，电影感远景，仰拍视角，强对比光影" \
  --duration 5 --ratio 16:9 --model_version seedance2.0 --poll 0

# ============================================================
# 镜头2（6s）：白龙在雷海中翻腾
# 【问题】目前没有白龙单体参考图，暂时用scene底图垫底+文生
# 建议后续补一张白龙全身彩铅/真人参考图后再重跑此镜头
# ============================================================
echo ">>> [镜头2] 白龙渡劫 — image2video（暂无白龙单体参考，用场景底图）"
dreamina image2video \
  --image "$POMIAO" \
  --prompt "影视真人风格，古风玄幻，电影感光影。巨大白龙在雷海中翻腾，龙身修长威严，白色龙鳞带银光，赤红龙眼，天空被雷电照亮，古风玄幻史诗感，大远景，平视视角" \
  --duration 6 --model_version seedance2.0 --poll 0

# ============================================================
# 镜头3（5s）：九道天雷劈落击中白龙
# 同上，白龙参考图缺失，用场景底图垫底
# ============================================================
echo ">>> [镜头3] 天雷击龙 — image2video（暂无白龙单体参考）"
dreamina image2video \
  --image "$POMIAO" \
  --prompt "影视真人风格，古风玄幻，电影感光影。九道天雷同时劈落击中巨大白龙，白龙半边龙身被轰碎，龙血飞溅，画面震撼惨烈，强冲击力，中远景，仰拍视角，动态爆发瞬间" \
  --duration 5 --model_version seedance2.0 --poll 0

# ============================================================
# 镜头4（5s）：白龙坠地砸进破庙
# 同上，白龙参考图缺失，用场景底图垫底
# ============================================================
echo ">>> [镜头4] 龙坠破庙 — image2video（暂无白龙单体参考）"
dreamina image2video \
  --image "$POMIAO" \
  --prompt "影视真人风格，古风玄幻，电影感光影。巨大白龙从高空坠落砸进荒废的破庙废墟，白色龙鳞破碎染血，碎瓦飞溅，烟尘混着雨水，动态冲击力强，中景俯拍" \
  --duration 5 --model_version seedance2.0 --poll 0

# ============================================================
# 镜头5（6s）：白蛇蜷缩墙角
# 垫图：scene_pomiao_corner + 白蛇参考图（双垫）
# ============================================================
echo ">>> [镜头5] 白蛇蜷缩墙角 — image2video + 白蛇参考垫图"
dreamina image2video \
  --image "$POMIAO_CORNER" \
  --prompt "影视真人风格，古风玄幻，电影感光影。昏暗破庙墙角，白光散去，地上蜷缩着一条通体雪白的小蛇，约手臂长短，鳞片染血，虚弱濒死，赤红眼睛微微睁开，近景平视，氛围压抑凄凉。参考图为白蛇角色参考，蛇的姿态和比例必须与参考图一致。" \
  --duration 6 --model_version seedance2.0 --poll 0

# ============================================================
# 镜头6（4s）：女主推门登场
# 垫图：scene_pomiao + 沈映晚角色参考图（双垫）
# ============================================================
echo ">>> [镜头6] 女主推门登场 — image2video + 沈映晚参考垫图"
dreamina image2video \
  --image "$POMIAO" \
  --prompt "影视真人风格，古风玄幻，电影感光影。破庙门口，一位年轻娇小的古风女子（身穿粉红长裙素青衣裙，黑发束髻）背着竹制药篓推门而入，逆光中惊讶停步，中景平视，画面具有雨天潮湿感。参考图女子的相貌和服装必须与参考图完全一致。" \
  --duration 4 --model_version seedance2.0 --poll 0

# ============================================================
# 镜头7（4s）：蹲下试探蛇伤
# 垫图：scene_pomiao_corner + 沈映晚角色图（双垫）
# ============================================================
echo ">>> [镜头7] 蹲下试探蛇伤 — image2video + 沈映晚参考垫图"
dreamina image2video \
  --image "$POMIAO_CORNER" \
  --prompt "影视真人风格，古风玄幻，电影感光影。破庙昏暗角落，娇小的古风女子（粉红长裙素青衣裙）蹲在一条雪白小蛇（约手臂长短）面前，神情惊讶又心疼，伸手轻轻试探小蛇的伤口，近景俯拍，氛围细腻温柔。女子的相貌和服装必须与参考图完全一致。" \
  --duration 4 --model_version seedance2.0 --poll 0

# ============================================================
# 镜头8（5s）：抱蛇回家，名场面（seedance自带配音）
# 垫图：scene_pomiao_corner + 沈映晚角色图 + 白蛇角色图（三垫）
# ============================================================
echo ">>> [镜头8] 抱蛇回家（名场面）— image2video + 三重垫图"
dreamina image2video \
  --image "$POMIAO_CORNER" \
  --prompt "影视真人风格，古风玄幻，电影感光影。近景特写，昏暗破庙角落，古风女子（粉红长裙素青衣裙）轻轻将一条受伤的白蛇抱起，贴近怀中，白蛇蜷在她胸前，她低头温柔看着它，神情心疼又坚定。暖冷对比光影。人物开口说话：'别怕，我带你回家。'女子的相貌服装必须与参考图完全一致，白蛇的姿态比例必须与参考图一致。" \
  --duration 5 --model_version seedance2.0 --poll 0

echo ""
echo "=== 全部提交完成 ==="
echo "注意：镜头2/3/4白龙无单体参考，图生视频效果可能有龙形态不一致问题"
echo "建议后续补一张白龙全身参考图（敖玄_人形 或 敖玄_白蛇_全身）后重跑"
